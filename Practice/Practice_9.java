package Practice;

public class Practice_9 {
    public static void main(String[] args) {
        int a = 100; // 정수
        double b = 10.5; // 실수
        boolean c = true; // 불리언(boolean) true, false 입력 가능함.
        char d = 'A'; // 문자 하나만 들어감.
        String e = "Hello java"; // 문자열을 다루기 위한 특별한 타입.

        System.out.println(a); // ctrl+d는 코드 한줄을 복사해서 붙일수있음.
        System.out.println(b); // ctrl+d는 코드 한줄을 복사해서 붙일수있음.
        System.out.println(c); // ctrl+d는 코드 한줄을 복사해서 붙일수있음.
        System.out.println(d); // ctrl+d는 코드 한줄을 복사해서 붙일수있음.
        System.out.println(e); // ctrl+d는 코드 한줄을 복사해서 붙일수있음.
    }
}
