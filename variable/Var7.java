package variable;

public class Var7{

    public static void main(String[] args) {
        int a = 100; // 정수
        double b = 10.5; // 실수
        boolean c = true; // 불리언(boolean) true, false 입력 가능함.
        char d= 'A';  // 문자 하나만 들어감.
        String e= "Hello Java"; // 문자열을 다루기 위한 특별한 타입.

        System.out.println(a);  // ctrl+d 는 코드 한줄을 복사해서 붙일수있음.
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

        /*
        코드에서 100, 10.5, true, 'A', "Hello Java"와 같은 고정된 값을 프로그래밍 용어로
        리터럴이라 함.
        */
    }
}


