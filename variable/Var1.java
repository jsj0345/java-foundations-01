package variable;  /* 패키지는 자바 파일을 구분하기 위한 폴더를 의미. , 소속된 패키지를 선언해줘야함.
                       자바 파일이 위치하는 패키지와 package variable 선언 위치가 같아야함
                   */
public class Var1 {

    public static void main(String[] args) { // public static void==psvm
        System.out.println(20);   // System.out.println은 sout이라함.
        System.out.println(20);   // 변경 10->20
        System.out.println(20);
    }
}




